CREATE FILE FORMAT csv_format TYPE=csv;

--To add the necessary permissions to the service account generated by snowflake to have access to the GCS Bucket
desc integration gcp_bucket_read



--To create an external stage on top of the integration so that we can refer it in our subsequent sql statements
create or replace stage rentals
url='gcs://snowflake_projects_kishalay/car_rentals_data/customers_daily/'
storage_integration=gcp_bucket_read
file_format=(format_name=csv_format)

