create database car_rental;

use car_rental;

Create table Dim_location(
location_key int autoincrement primary key,
location_id string unique not null,
location_name string
);

Create or replace table dim_customer(
customer_key int autoincrement primary key,
customer_id string unique not null,
name string,
email string,
phone string,
effective_date timestamp,
end_date timestamp,
is_current boolean
);

create table dim_date(
date_key int primary key,
date Date unique not null,
year int,
month int,
day int,
quarter int
);

create or replace table dim_car(
car_key int autoincrement primary key,
car_id string unique not null,
make string,
model string,
year int
);



create or replace table fact_rentals(
rental_id string primary key,
customer_key int,
car_key int,
pickup_loc_key int,
drop_loc_key int,
start_date_key int,
end_date_key int,
amount float,
quantity int,
rental_duration_days int,
total_rental_amount int,
average_rental_amount float,
is_long_rental boolean,
foreign key(customer_key) references dim_customer(customer_key),
foreign key(car_key) references dim_car(car_key),
foreign key(pickup_loc_key) references dim_date(date_key),
foreign key(drop_loc_key) references dim_date(date_key),
foreign key(start_date_key) references dim_date(date_key),
foreign key(end_date_key) references dim_date(date_key)



);



